    // Runtime routines
    // ────────────────────────────────────────────────────────────────────────

    fn emit_runtime(&mut self) {
        self.emit_print_int_clean();
        self.emit_print_newline();
        self.emit_bounds_error();
        self.emit_stack_errors();
        self.emit_queue_errors();
        self.emit_sort_routine();
        self.emit_reverse_routine();
    }

    fn emit_print_int_clean(&mut self) {
        self.emit("; ── runtime: _print_int ──────────────────────────────");
        self.emit("; Prints the signed 64-bit integer in RAX to stdout.");
        self.emit("_print_int:");
        self.emit("    push rbx");
        self.emit("    push rcx");
        self.emit("    push rdx");
        self.emit("    push rsi");
        self.emit("    push rdi");
        self.emit("    push r12");
        self.emit("");
        self.emit("    mov r12, rax            ; save original value");
        self.emit("    sub rsp, 24             ; local buffer on stack");
        self.emit("    lea rsi, [rsp+23]       ; rsi = pointer to end of buffer");
        self.emit("    mov byte [rsi], 0       ; null terminate (unused but safe)");
        self.emit("    mov rcx, 0              ; digit count");
        self.emit("");
        self.emit("    ; Handle negative");
        self.emit("    test rax, rax");
        self.emit("    jns .print_positive");
        self.emit("    neg rax");
        self.emit(".print_positive:");
        self.emit("    ; Convert to digits (reverse order)");
        self.emit(".print_digit_loop:");
        self.emit("    xor rdx, rdx");
        self.emit("    mov rbx, 10");
        self.emit("    div rbx");
        self.emit("    add dl, '0'");
        self.emit("    dec rsi");
        self.emit("    mov [rsi], dl");
        self.emit("    inc rcx");
        self.emit("    test rax, rax");
        self.emit("    jnz .print_digit_loop");
        self.emit("");
        self.emit("    ; Prepend '-' if original was negative");
        self.emit("    test r12, r12");
        self.emit("    jns .print_write");
        self.emit("    dec rsi");
        self.emit("    mov byte [rsi], '-'");
        self.emit("    inc rcx");
        self.emit(".print_write:");
        self.emit("    ; write(1, rsi, rcx)");
        self.emit("    mov rax, 1              ; sys_write");
        self.emit("    mov rdi, 1              ; stdout");
        self.emit("    mov rdx, rcx            ; length");
        self.emit("    syscall");
        self.emit("");
        self.emit("    add rsp, 24");
        self.emit("    pop r12");
        self.emit("    pop rdi");
        self.emit("    pop rsi");
        self.emit("    pop rdx");
        self.emit("    pop rcx");
        self.emit("    pop rbx");
        self.emit("    ret");
        self.emit("");
    }

