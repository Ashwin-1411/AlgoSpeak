    fn emit_sort_routine(&mut self) {
        // Bubble sort: rdi = pointer to array elements, rsi = length
        self.emit("; ── runtime: _sort_array (bubble sort) ───────────────");
        self.emit("; rdi = pointer to first element, rsi = number of elements");
        self.emit("_sort_array:");
        self.emit("    push rbx");
        self.emit("    push rcx");
        self.emit("    push rdx");
        self.emit("    push r8");
        self.emit("    push r9");
        self.emit("    push r10");
        self.emit("");
        self.emit("    mov r8, rsi             ; r8 = n");
        self.emit("    cmp r8, 1");
        self.emit("    jle .sort_done");
        self.emit("");
        self.emit("    mov r9, 0               ; i = 0");
        self.emit(".sort_outer:");
        self.emit("    cmp r9, r8");
        self.emit("    jge .sort_done");
        self.emit("    mov r10, 0              ; j = 0");
        self.emit("    mov rcx, r8");
        self.emit("    sub rcx, 1");
        self.emit("    sub rcx, r9             ; rcx = n - 1 - i");
        self.emit(".sort_inner:");
        self.emit("    cmp r10, rcx");
        self.emit("    jge .sort_next_i");
        self.emit("    ; Compare arr[j] and arr[j+1]");
        self.emit("    mov rax, r10");
        self.emit("    imul rax, 8");
        self.emit("    add rax, rdi");
        self.emit("    mov rbx, [rax]          ; arr[j]");
        self.emit("    mov rdx, [rax+8]        ; arr[j+1]");
        self.emit("    cmp rbx, rdx");
        self.emit("    jle .sort_no_swap");
        self.emit("    ; Swap");
        self.emit("    mov [rax], rdx");
        self.emit("    mov [rax+8], rbx");
        self.emit(".sort_no_swap:");
        self.emit("    inc r10");
        self.emit("    jmp .sort_inner");
        self.emit(".sort_next_i:");
        self.emit("    inc r9");
        self.emit("    jmp .sort_outer");
        self.emit(".sort_done:");
        self.emit("    pop r10");
        self.emit("    pop r9");
        self.emit("    pop r8");
        self.emit("    pop rdx");
        self.emit("    pop rcx");
        self.emit("    pop rbx");
        self.emit("    ret");
        self.emit("");
    }

    fn emit_reverse_routine(&mut self) {
        // In-place reversal: rdi = pointer, rsi = length
        self.emit("; ── runtime: _reverse_array ──────────────────────────");
        self.emit("; rdi = pointer to first element, rsi = number of elements");
        self.emit("_reverse_array:");
        self.emit("    push rbx");
        self.emit("    push rcx");
        self.emit("    push rdx");
        self.emit("");
        self.emit("    mov rcx, 0              ; left = 0");
        self.emit("    mov rdx, rsi");
        self.emit("    dec rdx                  ; right = n - 1");
        self.emit(".rev_loop:");
        self.emit("    cmp rcx, rdx");
        self.emit("    jge .rev_done");
        self.emit("    ; Swap arr[left] and arr[right]");
        self.emit("    mov rax, rcx");
        self.emit("    imul rax, 8");
        self.emit("    add rax, rdi");
        self.emit("    mov rbx, rdx");
        self.emit("    imul rbx, 8");
        self.emit("    add rbx, rdi");
        self.emit("    push qword [rax]");
        self.emit("    push qword [rbx]");
        self.emit("    pop qword [rax]");
        self.emit("    pop qword [rbx]");
        self.emit("    inc rcx");
        self.emit("    dec rdx");
        self.emit("    jmp .rev_loop");
        self.emit(".rev_done:");
        self.emit("    pop rdx");
        self.emit("    pop rcx");
        self.emit("    pop rbx");
        self.emit("    ret");
        self.emit("");
    }
